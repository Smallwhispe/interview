# ---------------------------------------------------------------------------
# 函数：判断 file_path 是否命中文件白名单
# ---------------------------------------------------------------------------
should_filter_file_path() {
  local file_path="$1"
  local pattern
  [[ -z "$file_path" ]] && return 1
  for pattern in "${DOMAIN_FILTER_FILE_PATH_PATTERNS[@]}"; do
    # shellcheck disable=SC2053
    if [[ "$file_path" == $pattern ]]; then
      return 0
    fi
  done
  return 1
}

# ---------------------------------------------------------------------------
# 函数：判断 domain 是否命中域名白名单
# ---------------------------------------------------------------------------
should_filter_domain() {
  local domain="$1"
  local pattern
  [[ -z "$domain" ]] && return 1
  for pattern in "${DOMAIN_FILTER_DOMAIN_PATTERNS[@]}"; do
    # shellcheck disable=SC2053
    if [[ "$domain" == $pattern ]]; then
      return 0
    fi
  done
  return 1
}

# ---------------------------------------------------------------------------
# 函数：综合判断一条 (file_path, domain) 问题是否应被过滤
#   命中文件白名单 OR 命中域名白名单 → 0（过滤）
# ---------------------------------------------------------------------------
should_filter_domain_issue() {
  local file_path="$1"
  local domain="$2"
  if should_filter_file_path "$file_path"; then
    return 0
  fi
  if should_filter_domain "$domain"; then
    return 0
  fi
  return 1
}

# ---------------------------------------------------------------------------
# 独立 CLI 模式：仅在脚本被直接执行（非 source）时生效
# 输入：每行 TAB 分隔，第 1 列 file_path，第 2 列 domain，第 3+ 列为附加上下文
# 输出：未命中白名单的行原样输出
# ---------------------------------------------------------------------------
if [[ "${BASH_SOURCE[0]}" == "$0" ]]; then
  while IFS=$'\t' read -r file_path domain rest || [[ -n "$file_path$domain$rest" ]]; do
    [[ -z "$file_path" && -z "$domain" ]] && continue
    if should_filter_domain_issue "$file_path" "$domain"; then
      continue
    fi
    if [[ -n "$rest" ]]; then
      printf '%s\t%s\t%s\n' "$file_path" "$domain" "$rest"
    else
      printf '%s\t%s\n' "$file_path" "$domain"
    fi
  done
fi
