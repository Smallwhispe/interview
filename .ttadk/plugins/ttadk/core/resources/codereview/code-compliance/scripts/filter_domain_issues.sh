#!/usr/bin/env bash
# filter_domain_issues.sh
#
# Domain 规则扫描结果过滤脚本（白名单/已知误报自动剔除）。
#
# 说明：
#   - 当 Domain 规则识别到一条疑似问题后，将 (file_path, domain) 传入本脚本，
#     若命中以下任一白名单则视为误报，应从最终报告中过滤掉：
#       1) file_path 匹配 DOMAIN_FILTER_FILE_PATH_PATTERNS 任一模式；
#       2) domain    匹配 DOMAIN_FILTER_DOMAIN_PATTERNS     任一模式。
#   - 匹配采用 bash `[[ $value == $pattern ]]` 的内置 glob 风格，`*` 可匹配
#     包括 `/` 在内的任意字符串。模式与 SKILL 规则文档（Domain.md）保持一致。
#
# 路径解析约定：
#   下文示例中的 `scripts/filter_domain_issues.sh` 是相对本 Skill 根目录的路径。
#   $SKILL_ROOT 由调用方（Agent / 扫描器）在运行时解析为本 Skill 的实际绝对路径，
#   不要把开发仓库内的相对路径（如 plugins/.../code-compliance-checker/）硬编码进调用脚本。
#
# 使用方式：
#   方式 A（推荐，作为库 source 后调用函数）：
#     source "$SKILL_ROOT/scripts/filter_domain_issues.sh"
#     if should_filter_domain_issue "$file_path" "$domain"; then
#       continue   # 命中白名单，丢弃本条问题
#     fi
#
#   方式 B（独立 CLI，使用 TAB 分隔的 stdin → stdout 流式过滤）：
#     printf '%s\t%s\n' "$file_path" "$domain" | \
#       bash "$SKILL_ROOT/scripts/filter_domain_issues.sh"
#     # 命中白名单的行被丢弃；其他原样输出到 stdout（可保留额外尾列）。
#
# 退出码（函数）：
#   0 → 命中白名单，应过滤（drop）
#   1 → 未命中，保留（keep）

